export function useI18n() {
  const [locale, setLocale] = useState<Locale>('en');
  const [isHydrated, setIsHydrated] = useState(false);
  
  useEffect(() => {
    // Mark as hydrated
    setIsHydrated(true);
    
    // Load locale from localStorage only after hydration
    const savedLocale = localStorage.getItem('locale') as Locale;
    if (savedLocale && locales.includes(savedLocale)) {
      setLocale(savedLocale);
    }
  }, []);

  const changeLocale = (newLocale: Locale) => {
    setLocale(newLocale);
    localStorage.setItem('locale', newLocale);
  };

  const t = (key: string): string => {
    // During SSR and before hydration, always use English to prevent hydration mismatch
    const currentLocale = isHydrated ? locale : 'en';
    const currentMessages = messages[currentLocale] as Record<string, string>;
    const englishMessages = messages.en as Record<string, string>;
    return currentMessages[key] || englishMessages[key] || key;
  };
  
  return {
    locale: isHydrated ? locale : 'en',
    changeLocale,
    t,
    locales,
    localeNames,
    isHydrated
  };
}